package hw2JavaBasic;

public class UseOfSingleOrDoubleQuotationInsideAString {

	public static void main(String[] args) {
		System.out.println(
				"Janet Yellen, Born in 1946, American economist and Chair of the US Federal Reserve, born in Brooklyn, New York");
		System.out.println(
				"\n-------------------------------------------------------------------------------------------------------------------");
		System.out.println("\t\"Janet Yellen, \n\tBorn in 1946," + " \n\'American economist\' and"
				+ "\tChair of the US Federal Reserve, born in \\Brooklyn, New York");
		System.out.println("\"I am Kazi Mostafij\"");
	}

}
